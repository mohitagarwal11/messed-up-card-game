const PICK = 1;

export function toWhiteCard(id: number): Card {
  return { id, color: 'white', text: WHITE_CARDS[id], pick: PICK };
}

export function toBlackCard(id: number): Card {
  return { id, color: 'black', text: BLACK_CARDS[id], pick: PICK };
}

export function getWhiteCard(id: number): Card {
  if (id < 0 || id >= WHITE_CARDS.length) {
    throw new Error(`Invalid white card id: ${id}`);
  }
  return toWhiteCard(id);
}

export function getBlackCard(id: number): Card {
  if (id < 0 || id >= BLACK_CARDS.length) {
    throw new Error(`Invalid black card id: ${id}`);
  }
  return toBlackCard(id);
}

function shuffle<T>(arr: T[]): T[] {
  const copy = [...arr];
  for (let i = copy.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}

export function pickRandomBlackCard(): Card {
  const id = Math.floor(Math.random() * BLACK_CARDS.length);
  return toBlackCard(id);
}

export function pickRandomWhiteCard(excludeIds: number[] = []): Card {
  const exclude = new Set(excludeIds);
  let pool = WHITE_CARDS.map((_, i) => i).filter((i) => !exclude.has(i));
  if (pool.length === 0) {
    pool = WHITE_CARDS.map((_, i) => i);
  }
  const id = pool[Math.floor(Math.random() * pool.length)];
  return toWhiteCard(id);
}

export function pickRandomWhiteCardIds(count: number): number[] {
  const indices = shuffle(WHITE_CARDS.map((_, i) => i));
  return indices.slice(0, Math.min(count, WHITE_CARDS.length));
}
